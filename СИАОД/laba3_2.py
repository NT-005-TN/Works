def max_subarray_sum(arr):
    """
    Алгоритм Кадане для поиска максимальной суммы непрерывного подмассива
    Возвращает: максимальную сумму, подмассив, начальный и конечный индексы
    """
    if not arr:
        return 0, [], 0, 0
    
    max_current = max_global = arr[0]
    start = end = temp_start = 0
    
    for i in range(1, len(arr)):
        # Решаем: начать новый подмассив с текущего элемента или продолжить предыдущий
        if arr[i] > max_current + arr[i]:
            max_current = arr[i]
            temp_start = i
        else:
            max_current += arr[i]
        
        # Обновляем глобальный максимум
        if max_current > max_global:
            max_global = max_current
            start = temp_start
            end = i
            
    return max_global, arr[start:end+1], start, end

